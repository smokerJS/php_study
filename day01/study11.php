<?
  // 연산자 - 대입연산자
  $num;
  $num = 11; // = 대입연산자 값을 대입한다.
  echo $num;
  echo "<br/>";
  echo $num+=20; // += 값을 더한 결과를 대입한다.
  echo "<br/>";
  echo $num-=20; // -= 값을 뺀 결과를 대입한다.
  echo "<br/>";
  echo $num*=2; // *= 값을 곱한 결과를 대입한다.
  echo "<br/>";
  echo $num/=2; // /= 값을 나눈 결과를 대입한다.
  echo "<br/>";
  echo $num%=2; // %= 값을 나누고 나머지를 대입한다.
  echo "<br/>";
  echo $num.="가나다라"; // .= 문자열을 더한 값을 대입한다.
?>

